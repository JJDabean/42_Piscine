int	ft_sqrt(int nb)
{
	int	n;

	n = 1;
	if (nb > 2147483647)
		return (0);
	if (nb == 0)
		return (1);
	while (nb > 0 && n * n <= nb)
	{
		if (n * n == nb)
			return (n);
		else if (n > 46340)
			return (0);
		else
			n++;
	}
	return (0);
}
